/**
 * Gera dinamicamente os <option> do datalist
 */
export function preencherListaVersoes() {
  const datalist = document.getElementById("versoes");
  if (!datalist) return;

  datalist.innerHTML = ""; // limpa lista antiga

  // cria um option inicial
  const optInicial = document.createElement("option");
  optInicial.value = "";
  optInicial.textContent = "Selecione uma versão";
  datalist.appendChild(optInicial);

  // adiciona as versões ordenadas
  Object.keys(versoes)
    .sort()
    .forEach(v => {
      const opt = document.createElement("option");
      opt.value = v;
      datalist.appendChild(opt);
    });
}

